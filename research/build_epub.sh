#!/bin/bash
set -e

# ── Настройки ──────────────────────────────────────────────────────────────
TITLE="VR-экосистема культурного наследия Санкт-Петербурга: Исследование рынка"
AUTHOR="Claude+DVN"
LANG="ru"
OUTPUT="book.epub"
COVER=""          # путь к обложке, например: cover.jpg
CSS=""            # путь к CSS, например: style.css

# ── Файлы в нужном порядке ─────────────────────────────────────────────────
CHAPTERS=(
  00_executive_summary.md
  01_глобальный_рынок_vr_ar.md
  02_российский_рынок_vr_ar.md
  03_vr_в_культуре_мировой_опыт.md
  04_государственные_vr_экосистемы.md
  05_технологический_стек_2025.md
  06_ai_генеративные_технологии.md
  07_платформы_дистрибуции.md
  08_доступность_и_инклюзия.md
  09_кадры_и_компетенции.md
  10_правовое_регулирование.md
  11_финансовые_модели.md
  12_риски_и_рекомендации.md
  13_дорожная_карта.md
)

# ── Проверка pandoc ────────────────────────────────────────────────────────
if ! command -v pandoc &>/dev/null; then
  echo "❌  pandoc не найден. Установи: brew install pandoc"
  exit 1
fi

# ── Сборка аргументов ──────────────────────────────────────────────────────
ARGS=()

for f in "${CHAPTERS[@]}"; do
  if [[ -f "$f" ]]; then
    ARGS+=("$f")
  else
    echo "⚠️  Файл не найден, пропускаю: $f"
  fi
done

ARGS+=(
  --metadata "title=$TITLE"
  --metadata "author=$AUTHOR"
  --metadata "lang=$LANG"
  --toc
  --toc-depth=2
  -o "$OUTPUT"
)

[[ -n "$COVER" && -f "$COVER" ]] && ARGS+=(--epub-cover-image="$COVER")
[[ -n "$CSS"   && -f "$CSS"   ]] && ARGS+=(--css="$CSS")

# ── Запуск ─────────────────────────────────────────────────────────────────
echo "📚  Собираю: $OUTPUT"
pandoc "${ARGS[@]}"
echo "✅  Готово → $(du -sh "$OUTPUT" | cut -f1)  $OUTPUT"
